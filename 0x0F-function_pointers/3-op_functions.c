#include "3-calc.h"

/**
 * op_add- add numbers
 * @a: operand
 * @b: operand
 * Return: int
 */
int op_add(int a, int b)
{
return (a + b);
}

/**
 * op_sub- subtract numbers
 * @a: operand
 * @b: operand
 * Return: int
 */
int op_sub(int a, int b)
{
return (a - b);
}

/**
 * op_mul- multiply numbers
 * @a: operand
 * @b: operand
 * Return: int
 */
int op_mul(int a, int b)
{
return (a * b);
}

/**
 * op_div- divide numbers
 * @a: operand
 * @b: operand
 * Return: int
 */
int op_div(int a, int b)
{
return (a / b);
}

/**
 * op_mod- modulo of numbers
 * @a: operand
 * @b: operand
 * Return: int
 */
int op_mod(int a, int b)
{
return (a % b);
}
